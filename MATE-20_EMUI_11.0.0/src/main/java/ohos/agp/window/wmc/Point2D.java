package ohos.agp.window.wmc;

public class Point2D {
    private float mX;
    private float mY;

    public Point2D(float f, float f2) {
        this.mX = f;
        this.mY = f2;
    }

    public float getX() {
        return this.mX;
    }

    public float getY() {
        return this.mY;
    }
}

package ohos.sysappcomponents.contact.entity;

public class NickName {
    private int id;
    private String nickName;

    public void setId(int i) {
        this.id = i;
    }

    public int getId() {
        return this.id;
    }

    public String getNickName() {
        return this.nickName;
    }

    public void setNickName(String str) {
        this.nickName = str;
    }
}
